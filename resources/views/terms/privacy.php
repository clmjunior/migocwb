        <p>A sua privacidade é uma das nossas principais preocupações. Nesta política de privacidade, vamos explicar como coletamos, usamos, armazenamos e protegemos as informações que você fornece ao utilizar a nossa plataforma de divulgação e inscrição em eventos.</p>

        <h2>Coleta de Informações</h2>
        <p>Ao se cadastrar em nossa plataforma, coletamos informações pessoais como nome, endereço de e-mail e dados de login. Também podemos solicitar informações adicionais, como data de nascimento, perfil profissional e foto de perfil. Essas informações nos ajudam a fornecer uma melhor experiência e personalização dos serviços oferecidos.</p>
        <p>Além disso, coletamos informações sobre os eventos aos quais você se inscreve, como a data, horário, localização e detalhes do evento. Essas informações são necessárias para facilitar o processo de inscrição e garantir a sua participação.</p>

        <h2>Uso das Informações</h2>
        <p>As informações que coletamos são utilizadas para:</p>
        <ul>
            <li>Personalizar e melhorar a sua experiência na plataforma;</li>
            <li>Permitir que você se inscreva e participe de eventos;</li>
            <li>Enviar notificações e informações relevantes sobre eventos, incluindo confirmação de inscrição, atualizações e lembretes;</li>
            <li>Fornecer suporte técnico e atendimento ao cliente;</li>
            <li>Analisar e compreender melhor o uso da plataforma para realizar melhorias contínuas.</li>
        </ul>

        <h2>Armazenamento e Proteção das Informações</h2>
        <p>As informações coletadas são armazenadas em servidores seguros e protegidas por medidas de segurança apropriadas, como criptografia de dados e controle de acesso. Tomamos todas as precauções razoáveis para garantir a segurança dos dados, porém, ressaltamos que nenhum método de transmissão pela internet, ou método de armazenamento eletrônico, é 100% seguro.</p>

        <h2>Compartilhamento de Informações</h2>
        <p>Não compartilhamos as suas informações pessoais com terceiros sem o seu consentimento, exceto em circunstâncias estritamente necessárias para cumprir as finalidades descritas nesta política de privacidade.</p>
        <p>Podemos compartilhar informações agregadas e anônimas para fins de análise de tendências, estatísticas e marketing.</p>

        <h2>Cookies</h2>
        <p>Utilizamos cookies para melhorar a sua experiência na plataforma, permitindo que você faça login automaticamente, lembre-se de suas preferências e personalize o conteúdo exibido. Você pode optar por desabilitar os cookies em seu navegador, mas isso pode afetar a funcionalidade da nossa plataforma.</p>

        <h2>Alterações nesta Política de Privacidade</h2>
        <p>Podemos atualizar esta política de privacidade periodicamente. Recomendamos que você reveja essa política regularmente para se manter informado sobre como as suas informações são protegidas e utilizadas.</p>
        <p>Ao utilizar a nossa plataforma, você concorda com os termos descritos nesta política de privacidade.</p>

